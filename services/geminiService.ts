export const changeClothingOnly = async (
    faceChangedImage: ImageFile, 
    clothingPrompt: string
): Promise<ImageFile | null> => {
    try {
        console.log('üîÑ [Gemini 2.5 Flash] Clothing-only transformation (ULTRA HAIR PRESERVATION) starting...');
        
        const prompt = getStrictClothingOnlyPrompt(clothingPrompt);
        const startTime = Date.now();

        const response = await ai.models.generateContent({
            model: 'gemini-2.5-flash-image-preview',
            contents: {
                parts: [
                    {
                        inlineData: {
                            data: faceChangedImage.base64,
                            mimeType: faceChangedImage.mimeType,
                        },
                    },
                    {
                        text: prompt,
                    },
                ],
            },
            config: {
                responseModalities: [Modality.IMAGE, Modality.TEXT],
                temperature: 0.05, // üîß ÎçîÏö± ÎÇÆÏùÄ Ïò®ÎèÑÎ°ú ÏùºÍ¥ÄÏÑ± Í∑πÎåÄÌôî
            },
        });
        
        const responseTime = Date.now() - startTime;
        console.log('‚ö° [Gemini 2.5 Flash] ULTRA HAIR PRESERVATION Clothing response time:', responseTime + 'ms');
        
        if (!response.candidates || !response.candidates[0] || !response.candidates[0].content) {
            throw new Error('Invalid API response structure');
        }
        
        for (const part of response.candidates[0].content.parts) {
            if (part.inlineData) {
                const originalBase64 = part.inlineData.data;
                const originalMimeType = part.inlineData.mimeType;
                
                try {
                    const cleanedImage = await ImageProcessor.cleanBase64Image(
                        originalBase64, 
                        originalMimeType
                    );
                    console.log('‚úÖ [Gemini 2.5 Flash] ULTRA HAIR PRESERVATION Clothing transformation completed in', responseTime + 'ms');
                    return cleanedImage;
                } catch (cleanError) {
                    console.warn('‚ö†Ô∏è Metadata cleaning failed, using original');
                    return {
                        base64: originalBase64,
                        mimeType: originalMimeType,
                        url: `data:${originalMimeType};base64,${originalBase64}`
                    };
                }
            }
        }
        
        throw new Error('No image data in clothing transformation response');

    } catch (error) {
        console.error("‚ùå [Gemini 2.5 Flash] ULTRA HAIR PRESERVATION Clothing transformation error:", error);
        throw error;
    }
};

